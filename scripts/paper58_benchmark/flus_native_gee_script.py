from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any


def _read_json(path: Path) -> dict[str, Any]:
    payload = json.loads(Path(path).read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise ValueError(f"export specs must be a JSON object: {path}")
    return payload


def _js_string(value: object) -> str:
    text = str(value).replace("\\", "\\\\").replace("'", "\\'")
    return f"'{text}'"


def _js_array(values: list[Any]) -> str:
    rendered = []
    for value in values:
        if isinstance(value, str):
            rendered.append(_js_string(value))
        else:
            rendered.append(json.dumps(value, ensure_ascii=False))
    return "[" + ", ".join(rendered) + "]"


def _var_name(*parts: object) -> str:
    raw = "_".join(str(part) for part in parts)
    return re.sub(r"[^0-9A-Za-z_]+", "_", raw)


def _description(area_id: str, layer_name: str) -> str:
    return f"{area_id}_{layer_name}"


def _filename_prefix(area_id: str, layer_name: str) -> str:
    return f"{area_id}_{layer_name}"


def _image_expression(layer: dict[str, Any], region_var: str) -> str:
    name = str(layer.get("name", "layer"))
    collection = str(layer.get("collection", ""))
    bands = layer.get("bands") if isinstance(layer.get("bands"), list) else []
    bands_js = _js_array(bands)
    operation = str(layer.get("operation", ""))
    year = layer.get("year")

    if operation == "select:elevation":
        return f"ee.Image({_js_string(collection)}).select({bands_js}).clip({region_var})"
    if operation == "ee.Terrain.slope":
        return f"ee.Terrain.slope(ee.Image({_js_string(collection)})).select({bands_js}).clip({region_var})"
    if operation == "ee.Terrain.aspect":
        return f"ee.Terrain.aspect(ee.Image({_js_string(collection)})).select({bands_js}).clip({region_var})"
    if year is not None:
        start = f"{int(year)}-01-01"
        end = f"{int(year) + 1}-01-01"
        return (
            f"ee.ImageCollection({_js_string(collection)})"
            f".filterDate({_js_string(start)}, {_js_string(end)})"
            f".filterBounds({region_var})"
            f".select({bands_js})"
            f".mosaic()"
            f".clip({region_var})"
        )
    raise ValueError(f"unsupported GEE raster layer expression for {name}")


def build_gee_javascript(
    export_specs_path: Path,
    drive_folder: str = "paper58_flus_native",
    limit: int | None = None,
) -> str:
    specs = _read_json(Path(export_specs_path))
    tasks = specs.get("tasks")
    if not isinstance(tasks, list):
        raise ValueError("export specs must contain a 'tasks' list")
    selected_tasks = tasks[: int(limit)] if limit is not None else tasks

    lines = [
        "// Generated by scripts/paper58_benchmark/write_flus_native_gee_js.py",
        "// Review task count and Drive quota before running in Earth Engine.",
        "",
    ]

    for task_index, task in enumerate(selected_tasks):
        if not isinstance(task, dict):
            raise ValueError("each export task must be an object")
        area_id = str(task.get("area_id", f"task_{task_index:04d}"))
        bbox = task.get("bbox")
        if not isinstance(bbox, list) or len(bbox) != 4:
            raise ValueError(f"task {area_id} must contain a four-value bbox")
        region_var = _var_name("region", task_index)
        lines.extend(
            [
                f"// {area_id}",
                f"var {region_var} = ee.Geometry.Rectangle({_js_array([float(v) for v in bbox])});",
            ]
        )
        for layer in task.get("gee_rasters", []):
            if not isinstance(layer, dict):
                continue
            layer_name = str(layer.get("name", "layer"))
            image_var = _var_name("img", task_index, layer_name)
            scale_m = int(layer.get("scale_m", task.get("target_scale_m", 100)))
            expression = _image_expression(layer, region_var)
            description = _description(area_id, layer_name)
            prefix = _filename_prefix(area_id, layer_name)
            lines.extend(
                [
                    f"var {image_var} = {expression};",
                    "Export.image.toDrive({",
                    f"  image: {image_var},",
                    f"  description: {_js_string(description)},",
                    f"  folder: {_js_string(drive_folder)},",
                    f"  fileNamePrefix: {_js_string(prefix)},",
                    f"  region: {region_var},",
                    f"  scale: {scale_m},",
                    "  maxPixels: 1e13",
                    "});",
                ]
            )
        lines.append("")

    return "\n".join(lines)
